package xia;

public class laibulei {
	private double weight;
	public laibulei() {
	}
	public laibulei(double weight) {
		this.weight=weight;
	}
	private class CowLeg{
		private double length;
		private String color;
		public CowLeg() {
		}
		public CowLeg(double length,String color) {
			this.length=length;
			this.color=color;
		}
		public void info() {
			System.out.println("当前牛腿的颜色是："+color+"高"+length);
			System.out.println("本牛腿所在的牛重："+weight);
		}
	}
	public void test() {
		CowLeg c=new CowLeg(1.12,"黑白相间");
		c.info();
		}
	public static void main(String[] args) {
		laibulei cow=new laibulei(378.9);
		cow.test();
	}
}
